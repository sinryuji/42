#include <unistd.h>

void	ft_is_negative(int n)
{
	char	c1;
	char	c2;

	c1 = 'N';
	c2 = 'P';
	if (n < 0)
		write(1, &c1, 1);
	if (n >= 0)
		write(1, &c2, 1);
}
