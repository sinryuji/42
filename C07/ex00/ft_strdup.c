#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		len;
	char	*result;
	int		i;

	len = 0;
	while (src[len])
		len++;
	result = malloc(sizeof(char) * (len + 1));
	if (!result)
		return (NULL);
	i = 0;
	while (src[i])
	{
		result[i] = src[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}	
